void printList(TITEM* first) {
    while (first) {
        printf("'%s'\n", first->m_Name);
        first = first->m_Next;
    }
}


TITEM* newItem(const char* name, TITEM* next) {
    if (!next) {
        next = (TITEM*) malloc(sizeof(next));
        next->m_Next = NULL;
        next->m_Name = (char*) name;
        return next;
    }

    TITEM* newItm = (TITEM*) malloc(sizeof(newItm));
    newItm->m_Next = next;
    newItm->m_Name = (char*) name;
    return newItm;
}

TITEM* findByIndex(TITEM* first, int index) {
    if (index < 0) return NULL;

    int ind = 0;
    while (first) {
        if (ind == index) break;
        first = first->m_Next;
        ind++;
    }
    return first;
}

int getIndex(TITEM* first, TITEM* item) {
    int index = 0;
    while (first) {
        if (first == item) break;
        first = first->m_Next;
        index++;
    }
    if (first) return index;
    return -1;
}

TITEM* swap(TITEM* first, TITEM* itm1, TITEM* itm2) {
    int index1 = getIndex(first, itm1);
    int index2 = getIndex(first, itm2);

    TITEM* par1 = findByIndex(first, index1 - 1);
    TITEM* par2 = findByIndex(first, index2 - 1);

    if (!par1) {
        first = itm2;
        par2->m_Next = itm1;
    }
    else if (!par2) {
        first = itm1;
        par1->m_Next = itm2;
    }
    else {
        par1->m_Next = itm2;
        par2->m_Next = itm1;
    }

    TITEM* next1 = itm1->m_Next;
    TITEM* next2 = itm2->m_Next;

    itm1->m_Next = next2;
    itm2->m_Next = next1;

    return first;
}

TITEM* sortListCmp(TITEM* l, int ascending, int (*cmpFn)(const TITEM*, const TITEM*)) {
    printList(l);
    TITEM* first = l;
    TITEM* m = l;

    while (l->m_Next) {
        int a = cmpFn(l, l->m_Next);
        if ((ascending && a > 0) || (!ascending && a < 0)) {
            first = swap(first, l, l->m_Next);
        }
        else {
            l = l->m_Next;
        }
    }
    printf("\n");
    printList(first);
    return first;
}

void freeList(TITEM* src) {
    while (src) {
        TITEM* temp = src->m_Next;
        free(src);
        src = temp;
    }
}




